public class Count2 {
    public static void main(String[] args) {
        int x = 2;
        for (int times = 1; times <= 4; times++) {
            System.out.print("2 times ");
            System.out.print(times);
            System.out.print(" = ");
            System.out.print(x * times); 
    }
        System.out.println("");
    }
}